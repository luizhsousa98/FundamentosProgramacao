package br.com.lh.fundamentos.variaveis;

/**
 * 
 * @author lhsousa Alguns tipos primitivos mais utilizados
 */
public class Aplicacao2 {

	public static void main(String[] args) {

		// inteiro médio
		int a = 1;

		// inteiro maior
		long b = 2L;

		// verdadeiro ou falso
		boolean c = false;

		// decimal menor
		float f = 4.5f;

		/// decimal médio
		double d = 5.67;

		// Caractere único
		char ch = 'B';

		/*
		 * Cadeia de caracteres não é um tipo primitivo mas sim uma classe, mas é
		 * utilizado para receber uma cadeia caracteres
		 */
		String s = "Luiz";

	}

}
